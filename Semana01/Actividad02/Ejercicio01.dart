void main() {

  //Declaración de cadena(String)
  String name = "Anthony";
  print('Declaración de tipo String -> $name');
  
  //Declaración dinamica con var
  var name2 = "Juan";
  print('Declaracion de tipo dinamica var -> $name2');
  
  //Declaración dinamic con const
  //Este se inicializa en compilación
  const name3 = "Maria";
  print('Declaracion de tipo dinamica const -> '+name3);
  
  
 //Declaracion dinamica con final
  //Este se inicializa durante la ejecución
  const name4 = "Ruben";
  print('Declaracion de tipo dinamica final ->' + name4);

}
 